package com.yy.lieyin.properties;

public class WebProperties {

    private String key;
    private String host;
    private String serviceAddUrl;
    private String serviceDelUrl;
    private String serviceUpdateUrl;
    private String serviceQueryUrl;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getServiceAddUrl() {
        return serviceAddUrl;
    }

    public void setServiceAddUrl(String serviceAddUrl) {
        this.serviceAddUrl = serviceAddUrl;
    }

    public String getServiceDelUrl() {
        return serviceDelUrl;
    }

    public void setServiceDelUrl(String serviceDelUrl) {
        this.serviceDelUrl = serviceDelUrl;
    }

    public String getServiceUpdateUrl() {
        return serviceUpdateUrl;
    }

    public void setServiceUpdateUrl(String serviceUpdateUrl) {
        this.serviceUpdateUrl = serviceUpdateUrl;
    }

    public String getServiceQueryUrl() {
        return serviceQueryUrl;
    }

    public void setServiceQueryUrl(String serviceQueryUrl) {
        this.serviceQueryUrl = serviceQueryUrl;
    }
}
